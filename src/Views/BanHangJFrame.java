                    BigDecimal ptgiam = (BigDecimal) tbsanpham.getValueAt(selectedR, 3);

                    BigDecimal giamAmount = giagoc.multiply(ptgiam.divide(BigDecimal.valueOf(100)));
                    BigDecimal giaMoi = giagoc.subtract(giamAmount).setScale(0, RoundingMode.HALF_UP);
